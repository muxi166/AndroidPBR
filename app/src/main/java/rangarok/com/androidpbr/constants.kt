//data class SphereData(val position: ArrayList<Vec3>, val uv: ArrayList<Vec2>, val normals: ArrayList<Vec3>, val indices: ArrayList<int>)
const val _Sphere_X_Segment = 64
const val _Sphere_Y_Segment = 64
fun GenerateSphereVertices(): FloatArray {
    val position = ArrayList<Vec3>()
    val uv = ArrayList<Vec2>()
    val normals = ArrayList<Vec3>()
    val indices = ArrayList<Int>()
    for (y in 0.._Sphere_Y_Segment) {
        for (x in 0.._Sphere_X_Segment) {
            val xSegment = x / _Sphere_X_Segment.toFloat()
            val ySegment = y / _Sphere_Y_Segment.toFloat()
            val xPos = cos(xSegment * 2.0f * Math.PI) * sin(ySegment * Math.PI)
            val yPos = cos(ySegment * Math.PI)
            val zPos = sin(xSegment * 2.0f * Math.PI) * sin(ySegment * Math.PI)

            position.add(Vec3(xPos, yPos, zPos))
            uv.add(Vec2(xSegment, ySegment))
            normals.add(Vec3(xPos, yPos, zPos))
        }
    }

    var oddRow = false
    for (y in 0 until _Sphere_Y_Segment) {
        if (!oddRow)
        // even rows: y == 0, y == 2; and so on
        {
            for (x in 0.._Sphere_X_Segment) {
                indices.add(y * (_Sphere_X_Segment + 1) + x)
                indices.add((y + 1) * (_Sphere_X_Segment + 1) + x)
            }
        } else {
            for (x in _Sphere_X_Segment downTo 0) {
                indices.add((y + 1) * (_Sphere_X_Segment + 1) + x)
                indices.add(y * (_Sphere_X_Segment + 1) + x)
            }
        }
        oddRow = !oddRow
    }

    val data = ArrayList<Float>()

    for (i in 0 until position.size) {
        data.add(position[i].x)
        data.add(position[i].y)
        data.add(position[i].z)
        if (uv.size > 0) {
            data.add(uv[i].x)
            data.add(uv[i].y)
        }
        if (normals.size > 0) {
            data.add(normals[i].x)
            data.add(normals[i].y)
            data.add(normals[i].z)
        }
    }

    return data.toFloatArray()


}

